/*Scrivere un programma che definisca una variabile di tipo bool, le assegni true e la stampi, successivamente assegnli false alla stessa variabile e la stampi di nuovo*/
#include <iostream>
using namespace std; 

int main ()
{
    bool a = true; 
    cout << "Valore vero: " << a << endl; 
    a = false; 
    cout << "Valore falso: " << a << endl; 
    return 0; 
}
